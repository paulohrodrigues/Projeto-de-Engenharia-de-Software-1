    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
        if(nomeC.getText().trim().equals("") || codigoC.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this, "Preencha todos os Campos", "Erro!", HEIGHT, null);
            return ;
        }
        
        DisciplinaController d = new DisciplinaController(nomeC.getText(), codigoC.getText());
        if(d.cadastrar()){
            JOptionPane.showMessageDialog(this, "Salvo com Sucesso!", "Sucesso!!", HEIGHT, null);
            //this.listar(d,"");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void listar(DisciplinaController d,String value){
            nomeP.setText(value);
            d.setNome(nomeP.getText());
            
            ArrayList<String> keys= new ArrayList<>(); 
            keys.add("nome");
            ArrayList<String> values= new ArrayList<>(); 
            values.add("%"+nomeP.getText()+"%");
            
            List<models.Disciplina> lista = d.buascar("Disciplina.findByNomeLike", keys, values);
            String dados="";
            for(models.Disciplina l: lista){
                dados+="ID: "+l.getId()+" Codigo: "+l.getCodigo()+" Nome: "+l.getNome()+"\n";
            }
            this.lista.setText(dados);
    }
    
