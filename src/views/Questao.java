    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
        if(enunciadoC.getText().trim().equals("") || respostaGuiaC.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this, "Preencha todos os Campos", "Erro!", HEIGHT, null);
            return ;
        }
        
        QuestaoAbertaController c = new QuestaoAbertaController(respostaGuiaC.getText(), enunciadoC.getText());
        if(c.cadastrar()){
            System.out.println("Salvo");
            JOptionPane.showMessageDialog(this, "Salvo com Sucesso!", "Sucesso!", HEIGHT, null);
            listar();
            c=null;
        }else{
            JOptionPane.showMessageDialog(this, "Verifique a conexao com o BD!", "Erro!", HEIGHT, null);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        if(
            enunciadoC.getText().trim().equals("") ||
            a.getText().trim().equals("") ||
            a.getText().trim().equals("") ||
            c.getText().trim().equals("") ||
            d.getText().trim().equals("")
        ){
            JOptionPane.showMessageDialog(this, "Preencha todos os Campos", "Erro!", HEIGHT, null);
            return ;
        }
        String corretaText = correta.getSelectedItem().toString();
        
        QuestaoFechadaController f = new QuestaoFechadaController(enunciadoC.getText(), corretaText, a.getText(), b.getText(), c.getText(), d.getText());
        if(f.cadastrar()){
            JOptionPane.showMessageDialog(this, "Salvo com Sucesso!", "Sucesso!", HEIGHT, null);
            listar();
            f=null;
        }else{
            JOptionPane.showMessageDialog(this, "Verifique a conexao com o BD!", "Erro!", HEIGHT, null);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

