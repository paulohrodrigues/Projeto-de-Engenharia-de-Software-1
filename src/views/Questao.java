    private void listar(){
        //enunciadoP.setText(d.getEnunciado());
        QuestaoController d = new QuestaoAbertaController(null,null);
        d.setEnunciado(enunciadoP.getText());
        ArrayList keys = new ArrayList();
        keys.add("enunciado");
        ArrayList values = new ArrayList();
        values.add("%"+d.getEnunciado()+"%");
        List<models.Questao> lista = d.buscar("Questao.findByEnunciadoLike",keys,values);
        String dados="";
        for(models.Questao l: lista){
            dados+="ID: "+l.getId()+" Enunciado: "+l.getEnunciado()+"\n";
        }
        this.lista.setText(dados);
        d=null;
    }
    
